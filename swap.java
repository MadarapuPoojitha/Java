class Main
{
        public static void main(String args[] )
        {
        int a=15,b=20,c;
        System.out.println("Before Swapping");
        System.out.println(a + "  " +b);
        c=a;
        a=b;
        b=c;
        System.out.println("After Swapping");
        System.out.println(a + "   " +b);
        }
}

        